            <h4>Beginners, here are the 10 most important training elements you must master, and eight-week full-body beginner's program to start you off on the right foot.</h4> <br>
                          <p>If you are a beginner the routine below is ideal for you. DO NOT imitate routines of advanced bodybuilders! You'll only grow LESS in MORE time! Combined with performance nutrition, smart and hard training and sufficient rest a "lean tissue" gain of 24-36 pounds is realistic in the first two years of consistent training.</p>
             
<h4> 1. Work The Major Muscle Groups</h4>
              <p> Chances are you started lifting to get a bigger chest and arms, or strengthen your body for a particular sport. But working all the major muscle groups—chest, back, shoulders, quads, glutes, hamstrings, biceps, triceps (smaller muscle groups include the forearms, calves, abs)—allows you to build a symmetrical physique. This complete training also prevents muscular imbalances that can arise when you favor some body parts over others. Imbalances are no joke: They can lead to serious injuries.</p>
              
              <h4>2. Practice Form First</h4>
              
              <p>Many of the basic movements presented here may be new to you. As a result, your coordination may be challenged at times. If so, don't worry. Keep practicing and rehearsing the movement pattern and soon it will feel like second nature—and that's when your gains in size and strength will take off. Until then, it's important to understand and practice the basic movements before using more challenging weights.</p>
              <h4>3. Multi-Joint Exercises Are Superior Over Single-Joint Moves</h4>
              
              <p> Exercises can be categorized into two classes: multi-joint and single-joint. The distinction is that with multi-joint exercises, two (or more) sets of joints work to accomplish the lift. With a single-joint move, only one set of joints is working. During a bench press, for example, action is taking place at both the elbow and shoulder joints, while in a barbell curl the movement is restricted to the elbow joint. Because more muscle is engaged when doing multi-joint moves, you can use far heavier weights and they're therefore better for achieving muscle and strength gains.</p>
              
              <h4>4. Do Multiple Sets Of An Exercise</h4>
              
              <p>There are decades of scientific research now available on resistance training, and the evidence points to doing 3-4 sets of a given exercise for maximum benefit. Typically you should do a warm-up set or two of that movement before tackling more challenging weights.</p>
              <h4>5. Too Heavy Or Too Light Is Too Bad</h4>
              
              <p>So how much weight should you lift? Warm-ups are always done with light weight to get the target muscles working in coordination. As a beginner, you'll choose light weights so you can do about 15 reps on each set. The last few reps should be very difficult to achieve but you should always be using "good form."

As you progress, you should use even more challenging weights, ones in which you reach muscle failure by 8-12 reps with good form. If you can do more than that number, the weight is too light. Individuals training for strength choose even heavier weights, typically doing fewer than 6 reps. Remember to never sacrifice form to lift a weight that's too heavy.</p>
