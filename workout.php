            <h4>6. Control The Rep</h4>
            <p>The approach you should follow on each and every rep is like this: Inhale and hold your breath as you lift the weight in a strong and forceful manner, exhaling only over the top portion of the movement. Then lower the weight under control as you breathe in. Reverse direction smoothly at the bottom position, never bouncing the weight at the bottom.</p>
              
              <h4>7. Take A Short Rest Between Sets</h4>
              <p>Your muscles fatigue during a set. They need time to clear the lactic acid and changes in the pH that build up in the surrounding tissue. This usually takes 90-120 seconds, though larger body parts like legs and back may take longer, and smaller muscle groups like arms and calves may take less. A good measure: when you catch your breath and feel ready to go, start a new set.</p>
              
              <h4>8. Get At Least 48 Hours Rest Between Workouts</h4>
              <p>This is key: The lifting you do at the gym is the stimulus that begins a chain of events that causes the muscle to repair itself and make itself stronger the next time you hit the gym. That takes time, nutrients and rest. Clearly, then, you can't train too often, and dismissing good nutritional habits and shortchanging your rest will be counterproductive to your muscle gains. In terms of training frequency, don't hit a given muscle group more frequently than once every 48 hours. As you become more advanced and add volume, you'll want to increase that rest period.</p>
              
              <h4>9. Strive To Do A Little More Each Workout</h4>
              <p>If your body responds to the training stimulus by growing stronger but you do the same weight for the same reps each workout, it's just not going to grow any further. Hence, you can't fall into the rut of repeating the same workout week-in and week-out. Strive to do more reps with a given weight or increase the weight from one workout to the next—that's the progressive stimulus you need to keep making positive improvements.</p>
              
              <h4> 10. Follow This Plan For 8 Weeks</h4>
                          <p>As a beginner you'll make noticeable jumps in strength the first two months, but typically gains start to stall when following any given program for too long. That's when it's time to make some larger changes in your training.

After the novice period, you can change your training split by re-arranging muscle groups and the number of exercises, sets, and reps on given days. Manipulating all these variables keeps your training fresh and offers new challenges to overload the target muscles. This is an exciting time for beginners because there are few other times in your training career in which you'll see jumps in strength on your basic lifts from one workout to the next.</p>
