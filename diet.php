      <h3>Diet</h3>
      <p><img style="float: left; margin: 0 25px 15px 0" src="images/diet.jpg" alt="">Eating healthy but without exercise means you’re missing out on a big piece of the fitness puzzle. By the same token, working out isn’t nearly as effective if the right foods aren’t a part of our diet. Let’s examine why diet is such a crucial part of any fitness regimen, and learn how to effectively combine both into a healthy diet and fitness routine.</p>

      <h4>Fuel the Muscles</h4>
      <br>

      <p>When breaking down muscles into separate components, we end up with 25% protein and 75% water, and the rest would be carbohydrates in the form of glycogen.

Clearly, staying hydrated is crucial for health, but if enough protein is not consumed, it is very difficult for the body to build new muscle tissue, no matter what. Fueling the muscles every day with foods that contain protein like almonds, lean meats, and spirulina is the best way to get results.</p>

        <h4> Stay Away From Processed Fats</h4>
      <p>Processed fats like the trans fat found in hydrogenated oils clog up the vascular system, making it difficult for the heart to pump enough oxygen-rich blood into muscles when working out. They also increase the risk of heart disease. Avoid them whenever possible.</p>

        <h4>Good Fat to the Rescue</h4>
      <p>Can fat actually be healthy? Absolutely. It’s a crucial source of energy, and our bodies need it to build cells and to keep the muscles moving smoothly. The key is to opt for healthy monounsaturated fats such as those found in olive oil, avocados, and nuts, and the omega-3-rich polyunsaturated fats found in salmon flax, and canola oil.</p>
        
        <h4> Are Carbs Really Evil?</h4>
      <p>Some people avoid carbohydrates at all costs, but it should really be about being more selective about the carbs consumed. Snack foods like cookies and cakes are generally made with low-fiber refined grains and are full of added sugar. The body digests both rather quickly, and the result is a sudden burst of energy, followed by a sugar crash.

In contrast, fruits, vegetables, beans, and whole grain breads have what you might call “slow carbs,” because the body has to work harder to absorb them due to their high fiber content. Slow carb absorption makes for the sort of long-lasting energy boost that you need to keep your endurance up at the gym.</p>
        
        <h4> Diet and Exercise: a Winning Combination</h4>
      <p>A healthy diet and a rigorous exercise routine are the perfect combination to help us look and feel our best. Combining the two of these is the best way to get in shape and feel great.</p>
